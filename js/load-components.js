/**
 * load-components.js — Eagle Logistics & Relocations
 * Production v1 · DRY Header + Footer
 * ─────────────────────────────────────────────────────────────
 * Single source of truth for the shared Header and Footer.
 * Injects them into every page that contains:
 *   <div id="header-placeholder"></div>  — top of <body>
 *   <div id="footer-placeholder"></div>  — bottom of <body>, right
 *                                          before <script src="js/main.js">
 *
 * Runs synchronously (plain <script>, not deferred) so the
 * header/footer exist in the DOM before first paint — no
 * flash-of-missing-header, no layout jump.
 *
 * Handles:
 *   1. Header + Footer HTML injection
 *   2. Active nav-link highlighting (aria-current, based on the
 *      current page, computed automatically)
 *   3. Mobile nav toggle (hamburger open/close, body scroll lock)
 *   4. Footer "year" auto-update
 *
 * Base URL:
 *   All absolute references (canonical/share links, structured
 *   data, etc.) generated by this script use BASE_URL below.
 *   Update this single constant if the domain ever changes.
 * ─────────────────────────────────────────────────────────────
 */
(function () {
  "use strict";

  /* ── GLOBAL BASE URL ─────────────────────────────────────────
     Single source of truth for the project's live domain. Used
     for every absolute link/asset reference this script builds
     (organization schema, canonical home link, share/social
     fallbacks). Relative, root-aware links below still use the
     PATH HELPERS so the site keeps working locally / on preview
     deployments too.                                            */
  var BASE_URL = "https://slcinfo.in";

  /* ── PATH HELPERS ────────────────────────────────────────────
     Works out how deep the current page sits relative to the
     project root by counting path segments, so every link/asset
     path resolves correctly whether the page lives at the root
     (index.html, about.html, ...) or inside any nested subfolder
     (e.g. /pages/, /pages/services/, etc.) without needing to
     hardcode folder names.                                      */
  var _segments = window.location.pathname.split("/").filter(Boolean);
  // Last segment is the file itself (e.g. "about.html") or, for a
  // directory-style URL ending in "/", there is no file segment.
  var _hasFile = _segments.length > 0 && _segments[_segments.length - 1].indexOf(".") !== -1;
  var _depth = Math.max(_hasFile ? _segments.length - 1 : _segments.length, 0);
  var _root = _depth > 0 ? new Array(_depth + 1).join("../") : ""; // prefix back to project root
  var _p = function (file) { return _root + file; }; // resolve any ROOT-RELATIVE file from any depth

  /* ── PAGE LOCATIONS ──────────────────────────────────────────
     _p() only knows how to climb back UP to the project root —
     it has no idea where any given page actually lives once you
     get there. Every page this script links to (nav, footer,
     logo-home-link) must declare its real root-relative path
     here. Update this map (not the call sites) if a page is ever
     moved again, e.g. into /pages/, /pages/legal/, etc.          */
  var PAGE_PATHS = {
    "index.html": "index.html",
    "about.html": "pages/about.html",
    "services.html": "pages/services.html",
    "contact.html": "pages/contact.html",
    "privacy.html": "pages/privacy.html",
    "terms.html": "pages/terms.html"
  };
  // Resolve a logical page name (e.g. "about.html") to a working href
  // from the current page's depth, honoring PAGE_PATHS above.
  var _page = function (name) { return _p(PAGE_PATHS[name] || name); };

  /* ── CURRENT PAGE (for nav/footer active-state highlighting) ── */
  var _currentFile = (_segments[_segments.length - 1] || "index.html");
  if (_currentFile.indexOf(".") === -1) _currentFile = "index.html";
  var _pageKey = _currentFile.replace(".html", "") || "index";

  /* ── 1. HEADER HTML ──────────────────────────────────────── */
  var NAV_LINKS = [
    { key: "index", file: "index.html", label: "Home" },
    { key: "about", file: "about.html", label: "About" },
    { key: "services", file: "services.html", label: "Services" },
    { key: "contact", file: "contact.html", label: "Contact" }
  ];

  var navLinksHTML = NAV_LINKS.map(function (link) {
    var current = link.key === _pageKey ? ' aria-current="page"' : "";
    return '<li><a href="' + _page(link.file) + '" data-page="' + link.key + '"' + current + ">" + link.label + "</a></li>";
  }).join("\n          ");

  var HEADER_HTML =
    '<header class="site-header">' +
    '  <div class="container">' +
    '    <a href="' + _page("index.html") + '" class="brand" aria-label="Eagle Logistics &amp; Relocations — home">' +
    '      <img src="' + _p("assets/images/logo.png") + '" alt="Eagle Logistics &amp; Relocations" width="300" height="56">' +
    "    </a>" +
    '    <nav class="nav-primary" id="primary-nav" aria-label="Primary">' +
    '      <ul class="nav-links">' +
    "        " + navLinksHTML +
    "      </ul>" +
    '      <div class="header-cta">' +
    '        <a class="header-phone" href="tel:+919696301338">' +
    '          <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M22 16.92v3a2 2 0 0 1-2.18 2 19.79 19.79 0 0 1-8.63-3.07 19.5 19.5 0 0 1-6-6 19.79 19.79 0 0 1-3.07-8.67A2 2 0 0 1 4.11 2h3a2 2 0 0 1 2 1.72c.127.96.361 1.903.7 2.81a2 2 0 0 1-.45 2.11L8.09 9.91a16 16 0 0 0 6 6l1.27-1.27a2 2 0 0 1 2.11-.45c.907.339 1.85.573 2.81.7A2 2 0 0 1 22 16.92z"/></svg>' +
    "          +91 96963 01338" +
    "        </a>" +
    '        <a href="' + _page("contact.html") + '" class="btn btn-primary">Get a Quote</a>' +
    "      </div>" +
    "    </nav>" +
    '    <button class="nav-toggle" aria-label="Toggle navigation menu" aria-expanded="false" aria-controls="primary-nav">' +
    "      <span></span><span></span><span></span>" +
    "    </button>" +
    "  </div>" +
    "</header>";

  /* ── 2. FOOTER HTML ──────────────────────────────────────── */
  var FOOTER_HTML =
    '<footer class="site-footer">' +
    '  <div class="container">' +
    '    <div class="footer-top">' +
    '      <div class="footer-brand">' +
    '        <img src="' + _p("assets/images/logo-light.png") + '" alt="Eagle Logistics &amp; Relocations" width="246" height="46">' +
    "        <p>A India-based logistics and relocation startup helping families and businesses move between India and the UAE — with door-to-door coordination for freight, household shifting and customs documentation.</p>" +
    '        <ul class="footer-nap">' +
    "          <li>Add-A-254, Chungi No 2, Lalkuan, Pul Prahalapur, New Delhi, India</li>" +
    '          <li><a href="tel:+919696301338">+91 96963 01338</a></li>' +
    '          <li><a href="mailto:tiwarisangam5163@gmail.com">tiwarisangam5163@gmail.com</a></li>' +
    "        </ul>" +
    '        <div class="social-row" aria-label="Social media">' +
    '          <a href="https://www.linkedin.com/" target="_blank" rel="noopener noreferrer" aria-label="LinkedIn">' +
    '            <svg viewBox="0 0 24 24" fill="currentColor" width="16" height="16"><path d="M4.98 3.5a2.5 2.5 0 1 1 0 5.001 2.5 2.5 0 0 1 0-5.001zM3 9h4v12H3V9zm7 0h3.8v1.7h.05c.53-1 1.83-2.05 3.77-2.05 4.03 0 4.78 2.65 4.78 6.1V21h-4v-5.6c0-1.34-.02-3.06-1.87-3.06-1.87 0-2.16 1.46-2.16 2.96V21h-4V9z"/></svg>' +
    "          </a>" +
    '          <a href="https://twitter.com/" target="_blank" rel="noopener noreferrer" aria-label="X (Twitter)">' +
    '            <svg viewBox="0 0 24 24" fill="currentColor" width="16" height="16"><path d="M18.9 2H22l-7.6 8.7L22.5 22h-6.9l-5.4-7-6.2 7H1l8.1-9.3L1 2h6.9l4.9 6.4L18.9 2zm-2.4 18h1.9L7.6 4H5.6l10.9 16z"/></svg>' +
    "          </a>" +
    '          <a href="https://facebook.com/" target="_blank" rel="noopener noreferrer" aria-label="Facebook">' +
    '            <svg viewBox="0 0 24 24" fill="currentColor" width="16" height="16"><path d="M22 12a10 10 0 1 0-11.6 9.9v-7H7.9V12h2.5V9.8c0-2.5 1.5-3.9 3.8-3.9 1.1 0 2.2.2 2.2.2v2.5h-1.3c-1.2 0-1.6.8-1.6 1.6V12h2.8l-.4 2.9h-2.4v7A10 10 0 0 0 22 12z"/></svg>' +
    "          </a>" +
    "        </div>" +
    "      </div>" +
    '      <div class="footer-col">' +
    "        <h4>Company</h4>" +
    "        <ul>" +
    '          <li><a href="' + _page("about.html") + '" data-page="about">About Us</a></li>' +
    '          <li><a href="' + _page("services.html") + '" data-page="services">Services</a></li>' +
    '          <li><a href="' + _page("contact.html") + '" data-page="contact">Contact</a></li>' +
    '          <li><a href="' + _page("contact.html") + '" data-page="contact">Get a Quote</a></li>' +
    "        </ul>" +
    "      </div>" +
    '      <div class="footer-col">' +
    "        <h4>Services</h4>" +
    "        <ul>" +
    '          <li><a href="' + _page("services.html") + '#india-uae">India &amp; UAE Relocation</a></li>' +
    '          <li><a href="' + _page("services.html") + '#household">Household Shifting</a></li>' +
    '          <li><a href="' + _page("services.html") + '#freight">Sea &amp; Air Freight</a></li>' +
    '          <li><a href="' + _page("services.html") + '#customs">Customs &amp; Documentation</a></li>' +
    "        </ul>" +
    "      </div>" +
    '      <div class="footer-col footer-newsletter">' +
    "        <h4>Stay Updated</h4>" +
    '        <p style="font-size:var(--fs-sm);line-height:1.5;color:var(--slate-300);margin-top:var(--sp-3);">Relocation tips and India–UAE shipping updates, occasionally — never spam.</p>' +
    '        <form action="#" method="post">' +
    '          <label for="footer-email" class="visually-hidden">Email address</label>' +
    '          <input id="footer-email" type="email" placeholder="Your email address" autocomplete="email" required>' +
    '          <button type="submit" aria-label="Subscribe">' +
    '            <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M5 12h14M13 5l7 7-7 7"/></svg>' +
    "          </button>" +
    "        </form>" +
    "      </div>" +
    "    </div>" +
    '    <div class="footer-bottom">' +
    "      <p>&copy; <span data-year>2026</span> Eagle Logistics &amp; Relocations. All rights reserved.</p>" +
    '      <div class="legal-links">' +
    '        <a href="' + _page("privacy.html") + '">Privacy Policy</a>' +
    '        <a href="' + _page("terms.html") + '">Terms &amp; Conditions</a>' +
    "      </div>" +
    "    </div>" +
    "  </div>" +
    "</footer>";

  /* ── 3. INJECT HEADER ────────────────────────────────────── */
  var headerSlot = document.getElementById("header-placeholder");
  if (headerSlot) {
    headerSlot.outerHTML = HEADER_HTML;
  }

  /* ── 4. INJECT FOOTER ────────────────────────────────────── */
  var footerSlot = document.getElementById("footer-placeholder");
  if (footerSlot) {
    footerSlot.outerHTML = FOOTER_HTML;
  }

  /* ── 5. ACTIVE PAGE HIGHLIGHTING (footer quick links) ────── */
  document.querySelectorAll('.site-footer [data-page]').forEach(function (link) {
    if (link.getAttribute("data-page") === _pageKey) {
      link.setAttribute("aria-current", "page");
    }
  });

  /* ── 6. FOOTER YEAR ───────────────────────────────────────── */
  document.querySelectorAll("[data-year]").forEach(function (el) {
    el.textContent = new Date().getFullYear();
  });

  /* ── 7. MOBILE NAV TOGGLE ─────────────────────────────────── */
  var header = document.querySelector(".site-header");
  if (header) {
    var toggle = header.querySelector(".nav-toggle");
    var nav = header.querySelector(".nav-primary");

    if (toggle && nav) {
      toggle.addEventListener("click", function () {
        var open = toggle.getAttribute("aria-expanded") === "true";
        toggle.setAttribute("aria-expanded", String(!open));
        nav.classList.toggle("is-open", !open);
      });

      nav.querySelectorAll("a").forEach(function (link) {
        link.addEventListener("click", function () {
          toggle.setAttribute("aria-expanded", "false");
          nav.classList.remove("is-open");
        });
      });

      /* Close the dropdown on outside click / Escape, since it's now a
         small floating panel rather than a full-screen overlay. */
      document.addEventListener("click", function (e) {
        if (toggle.getAttribute("aria-expanded") !== "true") return;
        if (nav.contains(e.target) || toggle.contains(e.target)) return;
        toggle.setAttribute("aria-expanded", "false");
        nav.classList.remove("is-open");
      });
      document.addEventListener("keydown", function (e) {
        if (e.key === "Escape" && toggle.getAttribute("aria-expanded") === "true") {
          toggle.setAttribute("aria-expanded", "false");
          nav.classList.remove("is-open");
          toggle.focus();
        }
      });
    }

    /* ── 8. SCROLL-BASED HEADER STYLE ─────────────────────────
       Relies on the .is-scrolled transition already defined on
       .site-header in css/style.css, so the background fades
       smoothly both scrolling down (class added) and scrolling
       back up (class removed).                                  */
    function onScroll() {
      header.classList.toggle("is-scrolled", window.scrollY > 12);
    }
    onScroll();
    window.addEventListener("scroll", onScroll, { passive: true });
  }

  /* Expose BASE_URL globally in case other scripts (e.g. main.js,
     analytics snippets) need the canonical domain at runtime. */
  window.SITE_BASE_URL = BASE_URL;
})();